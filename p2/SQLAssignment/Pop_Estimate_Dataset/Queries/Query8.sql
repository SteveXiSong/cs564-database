
SELECT 
	NEW.DDESC, NEW.NAME, MAX(NEW.DIF)
FROM(
	SELECT 
		D.DIVISION_DESC AS DDESC, H.STNAME AS NAME, (H.HUEST_2011-H.HUEST_2010) AS DIF
	FROM 	
		DIVISION AS D ,
		HOUSING_UNITS_STATE_LEVEL AS H
	WHERE 
		H.DIVISION = D.DIVISION_CD
	ORDER BY
		D.DIVISION_DESC
		,
		DIF
	) AS NEW
	--select ordered table
GROUP BY
	NEW.DDESC
;	

